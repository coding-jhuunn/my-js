function rot13(str) {
  let capitalStr = str.toUpperCase();
  let convertedStr = "";
  for (let i = 0; i < capitalStr.length; i++) {
    for (let j = 0; j < alpha.length; j++) {
      if (capitalStr[i] === cipher[j]) {
        convertedStr += alpha[j];
        break;
      }
    }
  }
  return convertedStr;
}

